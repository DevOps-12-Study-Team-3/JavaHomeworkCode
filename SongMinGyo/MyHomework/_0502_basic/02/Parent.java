package HW_0502_basic_02;

public class Parent {
	long firstName;
	int bloodType;
	int height;
	String skinColor;
	
	public Parent() {
		
	}
	
	public Parent(long firstName, int bloodType, int height, String skinColor) {
		this.firstName = firstName;
		this.bloodType = bloodType;
		this.height = height;
		this.skinColor = skinColor;
	}
	
}
